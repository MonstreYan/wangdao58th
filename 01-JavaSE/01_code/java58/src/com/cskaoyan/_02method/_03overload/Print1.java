public class Print1 {
    // int  long double  float
    public static void print(int a) {
        System.out.println(a);
    }

    public static void print(long a) {
        System.out.println(a);
    }

    public static void print(double a) {
        System.out.println(a);
    }

    public static void print(float a) {
        System.out.println(a);
    }


}
